    private void guardarCompania(){
        Companias compania = new Companias();
        compania.setNombrecompanias(txtnombre.getText());
        compania.setNombrereducido(txtnombrered.getText());
        compania.setUrl(txturl.getText());
        compania.setProdcontacto(txtprodcont.getText());
        compania.setProdmail(txtprodmail.getText());
        compania.setProdtelef(txtprodtelef.getText());
        compania.setLogcontacto(txtlogcont.getText());
        compania.setLogmail(txtlogmail.getText());
        compania.setLogtelef(txtlogtelef.getText());
        compania.setSincontacto(txtsincont.getText());
        compania.setSinmail(txtsinmail.getText());
        compania.setSintelef(txtsintelef.getText());
        compania.setCobcontacto(txtcobcont.getText());
        compania.setCobmail(txtcobmail.getText());
        compania.setCobtelef(txtcobtelef.getText());
        compania.setProd1usu(prod1usu.getText());
        compania.setProd1clave(prod1clave.getText());
        compania.setProd2usu(prod2usu.getText());
        compania.setProd2clave(prod2clave.getText());
        compania.setProd3usu(prod3usu.getText());
        compania.setProd3clave(prod3clave.getText());
        String prod = CMBproductor.getSelectedItem().toString();
        int pro = Integer.parseInt(prod.substring(0, 4));
        compania.setProd1(pro);
        int resultado = AccionesCompanias.inserta(compania);
        if (resultado == 1){
            javax.swing.JOptionPane.showMessageDialog(this, "Registro guardado","Exito",javax.swing.JOptionPane.INFORMATION_MESSAGE);
//            limpiar.limpiarFormulario(this.getContentPane().getComponents());
        }
        else javax.swing.JOptionPane.showMessageDialog(this, "Algo salio mal!","Error",javax.swing.JOptionPane.ERROR_MESSAGE);
    }
    
    private void llenarComboProd(ArrayList<Productores> productores){
        for(int i=0;i<productores.size();i++){
            CMBproductor.addItem(productores.get(i).getId()+" "+productores.get(i).getNombre());
        }
    }
    private boolean validar(){
        if(txtnombre.getText().trim().equals(""))return false;
        else if (txtnombrered.getText().trim().equals(""))return false;
        else return true;
    }
