    private void llenarTabla (){
        DefaultTableModel asegurados = new DefaultTableModel();
        asegurados.addColumn("Nombre");
        asegurados.addColumn("DNI");
        asegurados.addColumn("Tel√©fono");
        TBLasegurados.setModel(asegurados);
        Long DNI = (long)100000001;
        String nombre = "";
        if(!TXTnombreODNI.getText().trim().equals("")){
            boolean letra=true;
            for(int i = 0;i<TXTnombreODNI.getText().length();i++){
                char c = TXTnombreODNI.getText().charAt(i);
                if((c<'a' || c>'z') && (c<'A' || c>'Z' ) && (c<' ' || c>' '))letra=false;
            }
            if(letra==false){
                DNI = Long.parseLong(TXTnombreODNI.getText());
                nombre = "";
            }
            else{
                nombre = TXTnombreODNI.getText();
            }
        }
        BuscarAsegurados lista = new BuscarAsegurados(nombre, DNI);
        lista.execute();
        ListaAsegurados listAseg = lista.getListAseg();
        for (int i=0;i<listAseg.getSize();i++){
            Object [] o = null;            
            asegurados.addRow(o);
            asegurados.setValueAt(listAseg.getAsegurado(i).getNombreasegurado(),i, 0);
            asegurados.setValueAt(listAseg.getAsegurado(i).getDNInumero(), i, 1);
            asegurados.setValueAt(listAseg.getAsegurado(i).getTele1(), i, 2);
        }
        System.out.println("llene tabla "+ new SimpleDateFormat("HH:mm:ss").format(new Date().getTime()));
        System.out.println("--------------------------------------------------------------");
    }

