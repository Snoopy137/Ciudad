    public static void BuscaUsuario (int id, String nombre){
        DefaultTableModel usuarios = new DefaultTableModel();
        tabusuario.setModel(usuarios);
        tabusuario.getTableHeader().setReorderingAllowed(false);
        try{
            ListaUsuarios list;
            tabusuario.removeAll();
            list = AccionesUsuarios.BuscaUsuario(id, nombre);
            usuarios.addColumn("ID");
            usuarios.addColumn("Nombre");
            usuarios.addColumn("Usuario");
            usuarios.addColumn("Contrase√±a");
            usuarios.addColumn("Privilegios");
            
            tabusuario.getColumnModel().getColumn(0).setMaxWidth(0);
            tabusuario.getColumnModel().getColumn(0).setMinWidth(0);
            tabusuario.getColumnModel().getColumn(0).setHeaderValue(0);
            tabusuario.getColumnModel().getColumn(0).setPreferredWidth(0);
            
            Object O[]=null;
            for (int i=0; i<list.getSize();i++){
                usuarios.addRow(O);
                Usuarios u = list.getUsuario(i);
                String contrasenia = u.getContrasenia().toString();
                String campo = "*";
                for(int x = 0;x<contrasenia.length();x++){
                    campo = campo + "*";
                }
                usuarios.setValueAt(u.getIdusuario(), i, 0);
                usuarios.setValueAt(u.getNombre(), i, 1);
                usuarios.setValueAt(u.getUsuario(), i, 2);
                usuarios.setValueAt(campo, i, 3);
                usuarios.setValueAt(u.getPriviliegios(), i, 4);
                
                usuarios.fireTableDataChanged();
            }
            
        }
        catch (Exception e){
            e.printStackTrace();
        }
    }
